package com.lsm.web.category;

public class CategoryVO {

	private int pNo;
	private String pParents;
	private String pChild;
	private String pName;
	
	public int getpNo() {
		return pNo;
	}
	public void setpNo(int pNo) {
		this.pNo = pNo;
	}
	public String getpParents() {
		return pParents;
	}
	public void setpParents(String pParents) {
		this.pParents = pParents;
	}
	public String getpChild() {
		return pChild;
	}
	public void setpChild(String pChild) {
		this.pChild = pChild;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	
}
