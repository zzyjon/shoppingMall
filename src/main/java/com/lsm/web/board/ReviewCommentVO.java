package com.lsm.web.board;

import java.util.Date;

public class ReviewCommentVO {

	private int rbcNo;	// 댓글 번호
	private int rNo;	// 게시글 번호
	private String rbcContent;	// 댓글 내용
	private String rbcWriter;	// 댓글 작성자
	private Date regdate;	// 댓글 등록일
	
	public int getRbcNo() {
		return rbcNo;
	}
	public void setRbcNo(int rbcNo) {
		this.rbcNo = rbcNo;
	}
	public int getrNo() {
		return rNo;
	}
	public void setrNo(int rNo) {
		this.rNo = rNo;
	}
	public String getRbcContent() {
		return rbcContent;
	}
	public void setRbcContent(String rbcContent) {
		this.rbcContent = rbcContent;
	}
	public String getRbcWriter() {
		return rbcWriter;
	}
	public void setRbcWriter(String rbcWriter) {
		this.rbcWriter = rbcWriter;
	}
	public Date getRegdate() {
		return regdate;
	}
	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}
	
	
}
